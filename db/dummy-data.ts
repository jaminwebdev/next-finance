export const getRandomDateInLast30Days = () => {
	const now = new Date();
	const pastDate = new Date(now);
	pastDate.setDate(now.getDate() - 30);

	const randomDate = new Date(
		pastDate.getTime() + Math.random() * (now.getTime() - pastDate.getTime())
	);
	return randomDate;
};
